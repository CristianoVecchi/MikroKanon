fun createArrayColorsFile(){
    val lines = readFileLineByLineUsingForEachLine()
    val totalArray = mutableListOf<Int>()
    val nColorSets = 685
    lines.forEach {
        if(it.contains('#')){
            val pos = it.indexOf('#')
            val hex = it.substring(pos+1, pos+7)
            val value = "0x" + hex
            //println(value)
            //println("$hex $value ${Integer.decode(value)}")
            totalArray.add(Integer.decode(value))
        }
    }
    val arrays = totalArray.take(nColorSets * 7).chunked(7)
//    arrays.forEachIndexed { index, list ->
//        println("$index: $list")
//        if(list.size != 7 ) println("WARNING")
//    }
    arrays.forEachIndexed { index, list ->
        println("$index ->  listOf(${list[0]},${list[1]},${list[2]},${list[3]},${list[4]},${list[5]},${list[6]})")
        if(list.size != 7 ) println("WARNING")
    }
}
